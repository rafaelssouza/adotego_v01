    private void initJComboBox_especie2() {
        
        List<Especie> lista_especie = new EspecieController().findAll();

        lista_especie.stream().forEach((especie) -> {
            jComboBox_especie2
                    .addItem(formatador
                            .formatarPrimeiraLetraMaiuscula(especie.getNome()));
        });
        
    }

