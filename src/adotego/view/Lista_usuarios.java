class Modelo_Tabela_Usuario extends AbstractTableModel {

    List<Usuario> usuarios;
    String colunas[] = new String[]{"Nome", "CÃ³digo", "CPF"};

    Modelo_Tabela_Usuario() {
        this.usuarios = new UsuarioController().findAll();
    }

    @Override
    public int getRowCount() {
        return usuarios.size();
    }

    @Override
    public int getColumnCount() {
        return colunas.length;
    }

    @Override
    public Object getValueAt(int rowIndex, int columnIndex) {
        Usuario u = usuarios.get(rowIndex);
        switch (columnIndex) {
            case 0:
                return u.getNome();
            case 1:
                return u.getId();
            case 2:
                return u.getCpf();
            default:
                return "--";
        }
    }

    @Override
    public String getColumnName(int column) {
        return colunas[column];
    }

    public int getIdIntoTheRow(JTable table) {
        int row = table.getSelectedRow();
        String val = String.valueOf(table.getValueAt(row, 1));
        return Integer.parseInt(val);
    }

}
